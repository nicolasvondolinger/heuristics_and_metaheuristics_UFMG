#include <bits/stdc++.h>

using namespace std;
 
#define _ ios_base::sync_with_stdio(0); cin.tie(0);
#define all(a) (a).begin(), (a).end()
#define endl '\n'
#define ff first
#define ss second
#define pb push_back
 
typedef long long ll;
 
const int INF = 0x3f3f3f3f;
const ll LINF = 0x3f3f3f3f3f3f3f3fll;

int euclidean_distance(double x1, double x2, double y1, double y2){
    double x = x1 - x2, y = y1 - y2;
    return round(sqrt(x*x + y*y));
}

double pseudoeuclian_distance(double x1, double x2, double y1, double y2){
    double x = x1 - x2, y = x1 - y2;
    double r = sqrt((x*x + y*y)/10.0);
    return round(r);
}

vector<pair<double, double>> positions;
vector<vector<int>> graph, edgeWeights;
vector<int> unionFind;
int n = 0;

int find(int v){
    if(unionFind[v] == v) return v;
    return unionFind[v] = find(unionFind[v]);
}

ll clarkeWright(int c){
    vector<pair<ll, pair<int, int>>> economy;
    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++)if(j!=c && i!=j){
            ll ans = edgeWeights[c][i] + edgeWeights[c][j] - edgeWeights[i][j];
            economy.pb({ans, {i, j}});
        }
    }

    sort(economy.begin(), economy.end(), greater<pair<ll, pair<int, int>>>());

    vector<int> count(n, 0);
    unionFind = vector<int>(n);
    for(int i = 0; i < n; i++) unionFind[i] = i;
    ll sum = 0;

    for(int i = 0; i < n; i++) sum += 2 * edgeWeights[c][i];
    
    for(int i = 0; i < economy.size(); i++){
        int u = economy[i].ss.ff, v = economy[i].ss.ss;
        if(count[u] == 2 || count[v] == 2 || find(u) == find(v)) continue;
        sum -= economy[i].ff;
        count[u]++; count[v]++;
        unionFind[find(v)] = find(u);
    }
    
    return sum;
}

int main(int argc, char* argv[]){ 
    string filename = argv[1];
    ifstream file(filename);
    
    if (!file.is_open()) {
        cerr << "Error: Could not open file " << filename << endl;
        return 1;
    }

    string line, keyword, value;
    string weightType;

    while (getline(file, line)) {
        istringstream iss(line); iss >> keyword;
        string discart; iss >> discart;
        
        if (keyword == "TYPE") {
            iss >> value;
            if (value != "TSP") {
                cerr << "Error: Unsupported problem type: " << value << endl;
                return 1;
            }
        } else if(keyword == "EDGE_WEIGHT_TYPE"){
            iss >> weightType;
            if(weightType != "EUC_2D" && weightType != "ATT"){
                cerr << "Error: Unsupported edge weight type " << weightType << endl;
                return 1;
            }
        } else if(keyword == "DIMENSION"){
            iss >> n; 
            graph = vector<vector<int>>(n);
            positions = vector<pair<double, double>>(n);
            edgeWeights = vector<vector<int>>(n, vector<int>(n, INF));
        } else if(keyword == "NODE_COORD_SECTION"){
            while (getline(file, line)){
                if (line == "EOF") break;
                istringstream temp(line);
                int p; double x, y; temp >> p >> x >> y;
                positions[p-1].ff = x; positions[p-1].ss = y;
            }
        } else if (keyword == "NAME" || keyword == "COMMENT" || keyword == "DISPLAY_DATA_TYPE") continue;
        else {
            cerr << "Error: Unsupported input type: " << keyword << " " << value << endl;
            return 1;
        }
    }

    for(int i = 0; i < n; i++){
        for(int j = 0; j < n; j++){
            if(i != j) graph[i].push_back(j);
        }
    }

    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < graph[i].size(); j++) {  
            int w = graph[i][j];
            double x1 = positions[i].first;
            double y1 = positions[i].second;
            double x2 = positions[w].first;
            double y2 = positions[w].second;

            if(weightType=="EUC_2D") edgeWeights[i][w] = euclidean_distance(x1, x2, y1, y2);
            else edgeWeights[i][w] = pseudoeuclian_distance(x1, x2, y1, y2);
        }
    } 

    ll ans = LINF;
    for(ll c = 0; c < n; c++) ans = min(ans, clarkeWright(c));
    cout << ans << endl;

    exit(0);
}
